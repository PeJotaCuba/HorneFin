let content = fs.readFileSync('utils/translations.ts', 'utf8');

for (const lang of ['ES', 'EN', 'PT']) {
  const langObj = translations[lang];
  let langSectionMatch = content.match(new RegExp(`${lang}: \\{[\\s\\S]*?\\n  \\},?\\n`));
  if (langSectionMatch) {
    let langSection = langSectionMatch[0];
    for (const [key, value] of Object.entries(langObj)) {
      if (!langSection.includes(`\n    ${key}:`)) {
        langSection = langSection.replace(/\n  \},?\n/, `,\n    ${key}: ${JSON.stringify(value)}\n  },\n`);
      }
    }
    content = content.replace(langSectionMatch[0], langSection);
  }
}

fs.writeFileSync('utils/translations.ts', content);
console.log('Translations updated successfully.');
